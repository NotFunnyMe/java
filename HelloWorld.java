public class HelloWorld {

    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");

            }
            System.out.print("\n");

        }

        for (int l = 0; l < 6; l++) {
            for (int m = 5; m >= l; m--) {

                System.out.print("*");

            }

            System.out.print("\n");

        }

        
    }
}
